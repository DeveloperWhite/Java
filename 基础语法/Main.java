

//public表示公开的
//class表示定义一个类
//Main表示定义的一个类名
public class Main {
    /*
    public表示公开的
    static表示静态的
    void表示空
    main表示定义的方法名为main
    （String【】 args）表示一个main方法的形式参数列表
     */
    public static void main(String[] args) {//定义一个公开的静态的主方法
        System.out.println("Hello world!");//方法体，必须以分号结尾
    }
}